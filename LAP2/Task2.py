

#volume tabung

print("Volume Tabung")
rt = int(input("Masukkan Jari-jari Lingkaran :"))
tt = int(input("Masukkan Tinggi :"))
pi = 3.14

Vt = pi*(rt*rt)*tt

print("Volume Tabung :", Vt)




